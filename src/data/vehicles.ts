export interface Vehicle {
  name: string;
  type: VehicleType;
  trunk: string; // raw string, e.g. "10+1", "17", ""
  seatsFolded: number | null;
}

export function parseTrunkValue(raw: string): number | null {
  if (!raw || raw.trim() === "") return null;
  return raw
    .split("+")
    .reduce((sum, part) => sum + (parseInt(part.trim(), 10) || 0), 0);
}

